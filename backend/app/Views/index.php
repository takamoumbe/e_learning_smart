            console.log(data);
            $("#error-message").show();
            $("#reussite").hide();
          } 
        },
        error: function(data) {
           console.log(data);
           $("#error-message").show();
           $("#reussite").hide();
           $("#submit-question").prop("disabled", false);

        }
      });

